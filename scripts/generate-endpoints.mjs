import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

import prettier from 'prettier';
import ts from 'typescript';
import { createRequire } from 'node:module';
import { WDT_ENDPOINTS } from '@waywake/wdt-sdk';

const require = createRequire(import.meta.url);
const sdkRoot = dirname(require.resolve('@waywake/wdt-sdk/package.json'));
const endpointTypesPath = resolve(sdkRoot, 'dist', 'endpoint-types.d.ts');

const __dirname = dirname(fileURLToPath(import.meta.url));
const outPath = resolve(__dirname, '..', 'nodes', 'WangDianApi', 'endpoints.generated.ts');

const endpoints = Object.values(WDT_ENDPOINTS).sort((a, b) => {
	if (a.category !== b.category) return a.category.localeCompare(b.category, 'zh-Hans-CN');
	return a.name.localeCompare(b.name, 'zh-Hans-CN');
});

const categories = [...new Set(endpoints.map((e) => e.category))];

const header = `// AUTO-GENERATED by scripts/generate-endpoints.mjs — do not edit by hand.
// Source: @waywake/wdt-sdk WDT_ENDPOINTS (${endpoints.length} endpoints across ${categories.length} categories)

export interface WdtEndpointOption {
	/** Friendly display name shown in the operation dropdown (e.g. 销售出库单查询). */
	name: string;
	/** The SDK method identifier, sent verbatim as the method query parameter. */
	value: string;
	/** Action label n8n surfaces in the UI subtitle. */
	action: string;
	/** Short description from the official API doc. */
	description: string;
	/** Category bucket used to filter the dropdown by resource. */
	category: string;
	/** Path inside the WangDian ERP client (informational). */
	clientPath: string;
	/** Link into the official API doc. */
	docUrl: string;
}

export interface WdtEndpointCategory {
	/** Category label, e.g. 订单类. */
	value: string;
	/** Default method value when the category is first selected. */
	defaultMethod: string;
}

export type WdtFieldType = 'string' | 'number' | 'boolean' | 'json';

export interface WdtFieldDef {
	/** Original camelCase field name sent to the SDK. */
	name: string;
	/** Display name surfaced in the n8n parameter UI. */
	displayName: string;
	/** Coarse n8n field type. */
	type: WdtFieldType;
	/** True when the TS member was optional (? token). */
	optional: boolean;
	/** Original TS type text (informational). */
	tsType: string;
	/** JSDoc description from the SDK type definition. */
	description: string;
}

export interface WdtEndpointParams {
	/** True when the request type is unstructured (WdtRequestBody) — fall back to a raw JSON editor. */
	fallbackToJson: boolean;
	/** Parsed field definitions, in declaration order. Empty when fallbackToJson is true or the interface has no own members. */
	fields: WdtFieldDef[];
}

export const WDT_ENDPOINT_CATEGORIES: WdtEndpointCategory[] = [
`;

const categoryLines = categories
	.map((category) => {
		const firstInCategory = endpoints.find((e) => e.category === category);
		return `\t{ value: ${JSON.stringify(category)}, defaultMethod: ${JSON.stringify(firstInCategory.method)} },`;
	})
	.join('\n');

const endpointLines = endpoints
	.map((e) => {
		const description = e.description || e.name;
		return `\t{
\t\tname: ${JSON.stringify(e.name)},
\t\tvalue: ${JSON.stringify(e.method)},
\t\taction: ${JSON.stringify(e.name)},
\t\tdescription: ${JSON.stringify(description)},
\t\tcategory: ${JSON.stringify(e.category)},
\t\tclientPath: ${JSON.stringify(e.clientPath)},
\t\tdocUrl: ${JSON.stringify(e.docUrl)},
\t},`;
	})
	.join('\n');

// --- Parse endpoint-types.d.ts for request field metadata ---
const sourceText = readFileSync(endpointTypesPath, 'utf8');
const sourceFile = ts.createSourceFile('endpoint-types.d.ts', sourceText, ts.ScriptTarget.Latest, true);

function typeKind(tsType) {
	switch (tsType) {
		case 'string':
			return 'string';
		case 'number':
			return 'number';
		case 'boolean':
			return 'boolean';
		default:
			return 'json';
	}
}

function jsDocText(node) {
	for (const tag of ts.getJSDocCommentsAndTags(node)) {
		if (ts.isJSDoc(tag)) {
			const comment = typeof tag.comment === 'string' ? tag.comment : '';
			return comment.trim();
		}
	}
	return '';
}

function splitCamel(name) {
	return name.replace(/([a-z0-9])([A-Z])/g, '$1 $2').replace(/([A-Z]+)([A-Z][a-z])/g, '$1 $2');
}

const fieldMap = new Map();
for (const stmt of sourceFile.statements) {
	if (!ts.isInterfaceDeclaration(stmt) || !stmt.name.text.endsWith('Request')) continue;
	const interfaceName = stmt.name.text;
	const fields = [];
	for (const member of stmt.members) {
		if (!ts.isPropertySignature(member) || !member.name || !ts.isIdentifier(member.name)) continue;
		const fieldName = member.name.text;
		const tsType = member.type ? member.type.getText(sourceFile).trim() : 'unknown';
		fields.push({
			name: fieldName,
			displayName: splitCamel(fieldName).replace(/^./, (c) => c.toUpperCase()),
			type: typeKind(tsType),
			optional: !!member.questionToken,
			tsType,
			description: jsDocText(member),
		});
	}
	fieldMap.set(interfaceName, fields);
}

function pascalOfMethod(method) {
	return method
		.split('.')
		.map((segment) => segment.charAt(0).toUpperCase() + segment.slice(1))
		.join('');
}

const paramsLines = endpoints
	.map((endpoint) => {
		const interfaceName = `Wdt${pascalOfMethod(endpoint.method)}Request`;
		const fields = fieldMap.get(interfaceName);
		if (!fields) {
			return `\t${JSON.stringify(endpoint.method)}: { fallbackToJson: true, fields: [] },`;
		}
		const fieldSerialised = fields
			.map((f) => {
				return `\t\t\t{
\t\t\t\tname: ${JSON.stringify(f.name)},
\t\t\t\tdisplayName: ${JSON.stringify(f.displayName)},
\t\t\t\ttype: ${JSON.stringify(f.type)},
\t\t\t\toptional: ${JSON.stringify(f.optional)},
\t\t\t\ttsType: ${JSON.stringify(f.tsType)},
\t\t\t\tdescription: ${JSON.stringify(f.description)},
\t\t\t},`;
			})
			.join('\n');
		return `\t${JSON.stringify(endpoint.method)}: {
\t\tfallbackToJson: false,
\t\tfields: [
${fieldSerialised}
\t\t],
\t},`;
	})
	.join('\n');

const fallbackCount = endpoints.filter((e) => !fieldMap.has(`Wdt${pascalOfMethod(e.method)}Request`)).length;
const totalFields = endpoints.reduce((sum, e) => sum + (fieldMap.get(`Wdt${pascalOfMethod(e.method)}Request`)?.length ?? 0), 0);

const rawBody = `${header}${categoryLines}
];

export const WDT_ENDPOINT_OPTIONS: WdtEndpointOption[] = [
${endpointLines}
];

export const WDT_ENDPOINT_FIELDS: Record<string, WdtEndpointParams> = {
${paramsLines}
};
`;

const prettierOptions = (await prettier.resolveConfig(outPath)) ?? {};
const body = await prettier.format(rawBody, {
	...prettierOptions,
	filepath: outPath,
});

writeFileSync(outPath, body);
console.log(
	`wrote ${outPath} (${endpoints.length} endpoints, ${categories.length} categories, ${totalFields} fields, ${fallbackCount} fallback to JSON)`,
);
